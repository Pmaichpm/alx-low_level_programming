#include "3-calc.h"

/**
 * op_add - returns the sum of two integers
 * @a: insert value
 * @b: insert value
 *
 * Return: sum of two integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the difference of two integers
 * @a: insert value
 * @b: insert value
 *
 * Return: difference of two integers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of two integers
 * @a: insert value
 * @b: insert value
 *
 * Return: product of two integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the division of two integers
 * @a: insert value
 * @b: insert value
 *
 * Return: division of two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the remainder of the division of two integers
 * @a: insert value
 * @b: insert value
 *
 * Return: remainder of a division of two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
